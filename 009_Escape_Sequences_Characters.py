# ----------------------------------------
# Escape Sequences Characters
# \/b => Back Space
# \newline => Escape new Line
# \\ => Escape Back Slash
# \' => Escape Single Quotes
# \" => Escape Double Quotes
# \n => Line Feed
# \ r => Carriage Return
# \ t => Horizontal Tab
# \xhh => Character Hex value
# ----------------------------------------

# Back space
print("Hello\bWorld")  # will remove o


# escap new line + \
print(
    "Hello \
      i love \
      python"
)

# escape back slash
print("I love back slach \\")

# Escape Single quote
print("I love Single Quote 'Test'  ")

# Escape Double quote
print('I love Single Quote \Test" ')

# Line Feed
print("Hello world\nSecond Line")

# Carriage Return
print("123456\rabcde")

# Horizontal Tab
print("Hello\tpython")

# [[[Character Hex value
print("\x53\x65\x72\x61\x6A")
#                                       www.Letter hex value.com]]]
 