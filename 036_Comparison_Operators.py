# --------------------------
# -- Comparison Operators --
# --------------------------
# [==]Equal
# [!=] Not Equal
# [>] Greater
# [<] Lesss Than
# [>=] Greater Than or Equal
# [<=] Less Than or Equal
# --------------------------

# Equal = Not Equal

print(100 == 100)
print(100 == 200)
print(100 == 100.00)

print("="*40)

print(100 != 100)
print(100 != 200)
print(100 != 100.00)

print("="*40)


# Greater Than + Less Than 

print(100 > 100)
print(100 > 200)
print(100 > 100.00)
print(100 > 40)


print("="*40)

print(100 < 100)
print(100 < 200)
print(100 < 100.00)
print(100 < 40)


print("="*40)


# Greater Than or Equal + Less Than or Equal

print(100 >= 100)
print(100 >= 200)
print(100 >= 100.00)
print(100 >= 40)


print("="*40)

print(100 <= 100)
print(100 <= 200)
print(100 <= 100.00)
print(100 <= 40)


print("="*40)
